/**
 * Parse a builtin insert snippet into a signature label + parameter names.
 * @param {BuiltinFn} fn
 * @returns {{ label: string, parameters: string[] }}
 */
function getBuiltinSignature(fn) {
  const insert = fn.insert || `${fn.name}()`;
  const open = insert.indexOf('(');
  const close = insert.lastIndexOf(')');
  if (open < 0 || close <= open) {
    return { label: `${fn.name}()`, parameters: [] };
  }
  const inside = insert.slice(open + 1, close);
  /** @type {string[]} */
  const rawParts = [];
  let cur = '';
  let depth = 0;
  for (const ch of inside) {
    if (ch === '{') {
      depth++;
      cur += ch;
    } else if (ch === '}') {
      depth--;
      cur += ch;
    } else if (ch === ',' && depth === 0) {
      rawParts.push(cur.trim());
      cur = '';
    } else {
      cur += ch;
    }
  }
  if (cur.trim()) {
    rawParts.push(cur.trim());
  }
  const parameters = rawParts
    .map(p =>
      p
        .replace(/\$\{\d+:([^}]+)\}/g, '$1')
        .replace(/\$\{\d+\}/g, '')
        .replace(/\$\d+/g, '')
        .trim()
    )
    .filter(Boolean);
  return {
    label: `${fn.name}(${parameters.join(', ')})`,
    parameters
  };
}

